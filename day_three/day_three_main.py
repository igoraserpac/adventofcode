from day_three_data import mapa

cont = 0
i = 0


def verificador(char):
    if char == '.':
        return False
    elif char == '#':
        return True


def contador(verif):
    global cont
    if verif:
        cont += 1


for linha in mapa:
    casa = linha[i]
    verif = verificador(casa)
    contador(verif)
    i += 3
    if i > 30:
        i = i - 31

print('--------------------------------------------PART ONE--------------------------------------------')
slop_b = cont
print(slop_b)
i = 0
cont = 0

